#DIGITAR LOS VALORES DE ALTURA Y BASE DEL RECTANGULO
alt = float(input("Escribir el valor de la altura del rectangulo= "))
bas = float(input("Escribe el valor de la base del rectangulo: "))
perimetro = 2*(alt + bas)
area = alt*bas 
print("El valor del perimetro es: ",perimetro)
print("El valor del area es: ",area)


